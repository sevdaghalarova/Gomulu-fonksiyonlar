# all() elimizde olan degerlerin hepsi "TRUE" ise True deger dondurur
# bir tane "False" varsa bile False deger dondurecek
liste=[0,1,2,3,4]
print(all(liste))

# Any()- elimizde olan degerlerin bir tanesi bile "True" ise True donderir
# hepsi false oldugu zaman False deger dondurur

print(any(liste))